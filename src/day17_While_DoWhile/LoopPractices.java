package day17_While_DoWhile;

public class LoopPractices {
    public static void main(String[] args) {

        for (int i = 0; i <=10 ; i++) {
            System.out.println("repeat "+i);
        }
        System.out.println("------------------------------------------------------------------------");
int j=0;
        while (j<=10){
            System.out.println(j);
       j++;
        }
        System.out.println("------------------------------------------------------------------------");

        int k =0;
        do {
            System.out.println(k);
            k++;
    }while (k<=100);


    }
}
