package day18_NestedLoop;

public class NestedLoop {
    public static void main(String[] args) {
        for (int i = 0; i <=10 ; i++) {
            System.out.println("Wooden Spoon");
            for (int j = 0; j <=2 ; j++) {
                System.out.println("Wooden Spoon2");

            }
        }


    }
}
