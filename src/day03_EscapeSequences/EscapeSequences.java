package day03_EscapeSequences;

public class EscapeSequences {
     /*
        escape sequences:Must be given with double quote if not it will not work
        \n:start a new line
        \t:tab button, Horizontal space creator(4 space is being created)
        //:back slash it creates 1 back slash.
        /":Double quote it creates single quote
         */



    public static void main(String[] args) {

        System.out.println(" Java \n Python \n C#");
        System.out.println("-----------------------------");

        System.out.println("Hello Cydeo\nHow are you \tall today?\nIt's nice to see you all!\nWhat class do we have next week.");
        System.out.println("----------------------------------");
        System.out.println("\\\\\\");
        System.out.println("-----------------------------------");
        System.out.print("\"");
        System.out.print("\"");
        System.out.print("\"");
        System.out.print("\"");
        System.out.println("\"");
        System.out.println("My favorite show is \"Kurtlar Vadisi\"\\ Game of throne");



    }
}
